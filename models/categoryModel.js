import mongoose from "mongoose";

const categorySchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
    },
    slug: {
      type: String,
      lowercase: true,
    },
  },
  { timestamps: true }
);

// ðŸ”´ THIS NAME MUST MATCH productModel ref
export default mongoose.model("Category", categorySchema);
